#ifndef __DIN_VIRTUAL_H__
#define __DIN_VIRTUAL_H__

//===================================================================================================================================================
//	Подключение библиотек QT
//===================================================================================================================================================
#include <QString>

//===================================================================================================================================================
//	Подключение модулей проекта
//===================================================================================================================================================
#include "service_manager/signals/din_signal.h"
#include "data_model/dc_controller.h"


//===================================================================================================================================================
//	Описание класса
//===================================================================================================================================================
//	Класс виртуальных дискретных входов
//  Дополнительный функционал - Пользовательское название сигнала
//===================================================================================================================================================
class DinVirtual : public DinSignal
{
public:
    //===============================================================================================================================================
    //	Конструктор класса
    //===============================================================================================================================================
    DinVirtual(DcController* controller, int32_t id, int32_t internalid, uint8_t direction, uint8_t type, uint8_t subtype, const QString &name, uint16_t subtypeID = 0);

    //===============================================================================================================================================
    //	Открытые методы класса
    //===============================================================================================================================================
    QString customName() const { return _name_param->value(); }
    void setCustomName(const QString& name) { _name_param->updateValue(name); }

    //===============================================================================================================================================
    //	Перегружаемые методы класса
    //===============================================================================================================================================
    virtual QString text() const override;
    virtual QString fullText() const override;

private:
    //===============================================================================================================================================
    //	Свойства класса
    //===============================================================================================================================================
    DcParam* _name_param;
};

#endif // __DIN_VIRTUAL_H__
