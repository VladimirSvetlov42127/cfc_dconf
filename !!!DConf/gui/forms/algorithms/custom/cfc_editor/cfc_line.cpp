//===================================================================================================================================================
//	Конструктор и деструктор класса
//===================================================================================================================================================
CfcLine::CfcLine(const QPointF& begin, const QPointF& end)
{
	//	Свойства класса
	_begin = begin;
	_end = end;

	//	Определение типа линии
    _type = DIAGONAL;
    if (_begin.x() == _end.x()) _type = VERTICAL;
    if (_begin.y() == _end.y()) _type = HORIZONTAL;
    if (_begin == _end) _type = NONE;

	return;
}


//===================================================================================================================================================
//	Открытые методы класса
//===================================================================================================================================================
bool CfcLine::intersects(const CfcLine line, QPointF& point) const
{
    QPointF d;
    auto it = QLineF(_begin, _end).intersects(QLineF(line.begin(), line.end()), &d);
    if (QLineF::BoundedIntersection == it && d != _begin && d != _end && d != line.begin() && d != line.end()) {
        point = d;
        return true; }

    return false;
}
