#ifndef __CFC_LINE_H__
#define __CFC_LINE_H__


//===================================================================================================================================================
//	Подключение библиотек QT
//===================================================================================================================================================
#include <QPointF>


//===================================================================================================================================================
//	Описание класса
//===================================================================================================================================================
//	Класс ортогональной линии для вывода соединений элементов
//===================================================================================================================================================
class CfcLine
{
public:
    //===============================================================================================================================================
    //	Списки данных
    //===============================================================================================================================================
    enum LineTypes : uint8_t { NONE, HORIZONTAL, VERTICAL, DIAGONAL };

    //===============================================================================================================================================
	//	Конструктор и деструктор класса
	//===============================================================================================================================================
    CfcLine(const QPointF& begin, const QPointF& end);

	//===============================================================================================================================================
	//	Методы работы со свойствами класса
	//===============================================================================================================================================
    void setType(uint8_t type) { _type = type; }
    uint8_t type() const { return _type; }
	QPointF begin() const { return _begin; }
	QPointF end() const { return _end; }

	//===============================================================================================================================================
	//	Открытые методы класса
	//===============================================================================================================================================
    bool intersects(const CfcLine line, QPointF& point) const;

private:
	//===============================================================================================================================================
	//	Свойства класса
	//===============================================================================================================================================
    uint8_t _type;
	QPointF _begin;
	QPointF _end;
};

#endif	// __CFC_LINE_H__
