EditorSocketData::EditorSocketData(FlexLogic::SocketType type, bool inversion)
{
    _uuid = QUuid::createUuid().toString(QUuid::Id128);
    _type = type;
    _inversion = inversion;
	_links_list.clear();
}

EditorSocketData::~EditorSocketData()
{
	_links_list.clear();
}
