#ifndef __GRAPHIC_PATH_H__
#define __GRAPHIC_PATH_H__


//===================================================================================================================================================
//	Подключение стандартных библиотек
//===================================================================================================================================================

//===================================================================================================================================================
//	Подключение сторонних библиотек
//===================================================================================================================================================

//===================================================================================================================================================
//	Подключение библиотек QT
//===================================================================================================================================================
#include <QList>
#include <QPointF>

//===================================================================================================================================================
//	Подключение модулей проекта
//===================================================================================================================================================
#include <gui/forms/algorithms/custom/flexlogic_namespace.h>
#include <gui/forms/algorithms/custom/Editor/graphic_line.h>


//===================================================================================================================================================
//	Описание класса
//===================================================================================================================================================
class GraphicPath
{
public:
	//===============================================================================================================================================
	//	Конструктор и деструктор класса
	//===============================================================================================================================================
	GraphicPath(QList<QPointF> points);
	~GraphicPath();

	//===============================================================================================================================================
	//	Методы работы со свойствами класса
	//===============================================================================================================================================
	QList<QPointF> points() { return _points; }
	void setPoints(QList<QPointF> points) { _points = points; }
	QPointF begin() { return _points.count() > 0 ? points().first() : QPointF(); }
	QPointF end() { return _points.count() > 0 ? points().last() : QPointF(); }

	//===============================================================================================================================================
	//	Открытые методы класса
	//===============================================================================================================================================
	QList<GraphicLine> graphicLines();
	void normalize();
	bool addNode(const QPointF& node);
	int selectedLine(const QPointF& point);
	bool moveLine(int& line, const QPointF& point);

private:
	//===============================================================================================================================================
	//	Вспомогательные методы класса
	//===============================================================================================================================================
	bool isOnLine(const QPointF& begin, const QPointF& end, const QPointF& node);
	void arrangePoints(QList<QPointF> old_points);

	//===============================================================================================================================================
	//	Свойства класса
	//===============================================================================================================================================
	QList<QPointF> _points;
};

#endif	// __GRAPH_PATH_H__
