//===================================================================================================================================================
//	Конструктор и деструктор класса
//===================================================================================================================================================
GraphicLine::GraphicLine(const QPointF& begin, const QPointF& end)
{
	//	Свойства класса
	_begin = begin;
	_end = end;

	//	Определение типа линии
	_type = FlexLogic::LineTypes::NONE;
	if (_begin.x() == _end.x()) _type = FlexLogic::LineTypes::VERTICAL;
	if (_begin.y() == _end.y()) _type = FlexLogic::LineTypes::HORIZONTAL;
	if (_begin == _end) _type = FlexLogic::LineTypes::HORIZONTAL;

	return;
}

GraphicLine::~GraphicLine()
{
}


//===================================================================================================================================================
//	Открытые методы класса
//===================================================================================================================================================
bool GraphicLine::intersects(const GraphicLine line, QPointF& point) const
{
	QPointF d;
	auto it = QLineF(_begin, _end).intersects(QLineF(line.begin(), line.end()), &d);
	if (QLineF::BoundedIntersection == it && d != _begin && d != _end && d != line.begin() && d != line.end()) {
		point = d;
		return true; }

	return false;
}
