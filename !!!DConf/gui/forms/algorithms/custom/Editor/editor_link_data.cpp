EditorLinkData::EditorLinkData(QString ID)
{
	if (!ID.isEmpty()) {
		int id = ID.toUInt();
		if (id >= _max) _max = id;
		_uuid = ID; }
	else _uuid = QString::number(++_max);

	_target_id = QString();
	_source_id = QString();
}

EditorLinkData::~EditorLinkData()
{
}
