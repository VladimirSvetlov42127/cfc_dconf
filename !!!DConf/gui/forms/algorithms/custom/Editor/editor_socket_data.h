#ifndef __EDITOR_SOCKET_DATA_H__
#define __EDITOR_SOCKET_DATA_H__


//===================================================================================================================================================
//	Подключение стандартных библиотек
//===================================================================================================================================================

//===================================================================================================================================================
//	Подключение сторонних библиотек
//===================================================================================================================================================

//===================================================================================================================================================
//	Подключение библиотек QT
//===================================================================================================================================================
#include <QString>
#include <QList>

//===================================================================================================================================================
//	Подключение модулей проекта
//===================================================================================================================================================
#include <gui/forms/algorithms/custom/flexlogic_namespace.h>


//===================================================================================================================================================
//	Описание класса
//===================================================================================================================================================
class EditorSocketData
{
public:
	//===============================================================================================================================================
	//	Конструктор и деструктор класса
	//===============================================================================================================================================
	EditorSocketData(FlexLogic::SocketType type, bool inversion = false);
	~EditorSocketData();

	//===============================================================================================================================================
	//	Методы работы со свойствами класса
	//===============================================================================================================================================
	QString ID() { return _uuid; }
	FlexLogic::SocketType SocketType() { return _type; }
	bool Inversion() { return _inversion; }
	QStringList SocketLinks() { return _links_list; }
	void SetID(QString id) { _uuid = id; };
	void SetType(FlexLogic::SocketType type) { _type = type; }
	void SetInversion(bool flag) { _inversion = flag; }

	//===============================================================================================================================================
	//	Методы работы со списком связей
	//===============================================================================================================================================
	void AppendLink(QString link_id) { if (_links_list.indexOf(link_id) < 0) _links_list.append(link_id); }
	void RemoveLink(QString link_id) { _links_list.removeOne(link_id); }

private:
	//===============================================================================================================================================
	//	Свойства класса
	//===============================================================================================================================================
	QString _uuid;
	FlexLogic::SocketType _type;
	bool _inversion;
	QStringList _links_list;
};

#endif	//	__EDITOR_SOCKET_DATA_H__
