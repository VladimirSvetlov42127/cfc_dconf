#ifndef __BYTE_CODE_H__
#define __BYTE_CODE_H__


//===================================================================================================================================================
//	Подключение стандартных библиотек
//===================================================================================================================================================
#include <cstdint>

//===================================================================================================================================================
//	Подключение сторонних библиотек
//===================================================================================================================================================

//===================================================================================================================================================
//	Подключение библиотек QT
//===================================================================================================================================================
#include <QByteArray>

//===================================================================================================================================================
//	Подключение модулей проекта
//===================================================================================================================================================


//===================================================================================================================================================
//	Описание класса
//===================================================================================================================================================
class ByteCode : public QByteArray
{
public:
	//===============================================================================================================================================
	//	Конструктор и деструктор класса
	//===============================================================================================================================================
	ByteCode();
	ByteCode(const char* data, qsizetype size = -1);
	ByteCode(qsizetype size, char ch);
	ByteCode(const QByteArray& other);
	ByteCode(QByteArray&& other);
	~ByteCode();

	//===============================================================================================================================================
	//	Открытые методы класса
	//===============================================================================================================================================
	void appendData(const uint16_t& value);
	void appendData(const uint32_t& value);
	void setData(int position, const uint16_t& value);
	void setData(int position, const uint32_t& value);

	//===============================================================================================================================================
	//	Специализированные методы класса
	//===============================================================================================================================================
	void appendComplexLength(const uint16_t& length);
	void appendComplexLength(const uint32_t& length);
	void setComplexLength(int position, const uint16_t& length);
	void setComplexLength(int position, const uint32_t& length);
};

#endif	//	__BYTE_CODE_H__
