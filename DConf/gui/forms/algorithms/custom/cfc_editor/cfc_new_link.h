#ifndef __CFC_NEW_LINK_H__
#define __CFC_NEW_LINK_H__


//===================================================================================================================================================
//	Подключение библиотек QT
//===================================================================================================================================================
#include <QGraphicsObject>
#include <QGraphicsItem>
#include <QWidget>
#include <QPointF>
#include <QList>
#include <QPainterPath>
#include <QPainter>
#include <QStyleOptionGraphicsItem>
#include <QPolygonF>
#include <QRectF>
#include <QGraphicsSceneMouseEvent>

//===================================================================================================================================================
//	Подключение модулей проекта
//===================================================================================================================================================
#include "gui/forms/algorithms/custom/cfc_editor/cfc_socket.h"


//===================================================================================================================================================
//	Описание класса
//===================================================================================================================================================
//  Класс отображения набора линий, который будет конвертирован в новое соединение
//===================================================================================================================================================
class CfcNewLink : public QGraphicsObject
{
	Q_OBJECT

public:
	//===============================================================================================================================================
	//	Конструктор и деструктор класса
	//===============================================================================================================================================
    CfcNewLink(CfcSocket* socket, /*const QPointF& mouse_point,*/ QGraphicsItem* parent = nullptr);

	//===============================================================================================================================================
	//	Открытые методы класса
	//===============================================================================================================================================
    CfcSocket* source() {return _source_socket;}
    CfcSocket* target() {return _target_socket;}
    QList<QPointF> points() const;
    virtual QRectF boundingRect() const override;
    virtual QPainterPath shape() const override;

    //===============================================================================================================================================
    //	Виртуальные методы класса
    //===============================================================================================================================================
    virtual void paint(QPainter* painter, const QStyleOptionGraphicsItem* option, QWidget* widget = nullptr) override;
    virtual void mouseReleaseEvent(QGraphicsSceneMouseEvent* event) override;
    virtual void mouseMoveEvent(QGraphicsSceneMouseEvent* event) override;

private:
    //===============================================================================================================================================
    //	Вспомогательные методы класса
    //===============================================================================================================================================
    QPointF firstPoint() const { return _current_socket->scenePos(); }
    QPolygonF polygon() const;
    QPainterPath path() const;

    //===============================================================================================================================================
    //	Свойства класса
    //===============================================================================================================================================
    CfcSocket* _current_socket;
    CfcSocket* _source_socket;
    CfcSocket* _target_socket;
    QPointF _mouse_point;
    bool _released;
};

#endif	//	__CFC_NEW_LINK_H__
