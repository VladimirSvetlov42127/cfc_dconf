#ifndef __EDITOR_ITEM_H__
#define __EDITOR_ITEM_H__

//===================================================================================================================================================
//	Подключение стандартных библиотек
//===================================================================================================================================================

//===================================================================================================================================================
//	Подключение сторонних библиотек
//===================================================================================================================================================

//===================================================================================================================================================
//	Подключение библиотек QT
//===================================================================================================================================================
#include <QList>
#include <QIcon>
#include <QString>

//===================================================================================================================================================
//	Подключение модулей проекта
//===================================================================================================================================================

//===================================================================================================================================================
//	Описание класса
//===================================================================================================================================================
class EditorItem
{
public:
	//===============================================================================================================================================
	//	Конструктор и деструктор класса
	//===============================================================================================================================================
	EditorItem(QString name, QIcon icon, QString index, EditorItem* parent_item = nullptr);
    ~EditorItem();

	//===============================================================================================================================================
	//	Методы работы со свойствами класса
	//===============================================================================================================================================
	QString Name() const { return _name; }
	QString Index() const { return _index; }
	QIcon Icon() const { return _icon; }

	//===============================================================================================================================================
	//	Открытые методы класса
	//===============================================================================================================================================
	void AppendChild(EditorItem* child) { _child_items.append(child); }
	EditorItem* Child(int row) { return _child_items.value(row); }
	int ChildCount() const { return _child_items.count(); }
	int row() const { return _parent_item ? _parent_item->_child_items.indexOf(const_cast<EditorItem*>(this)) : 0; }
	EditorItem* ParentItem() { return _parent_item; }

private:
	//===============================================================================================================================================
	//	Свойства класса
	//===============================================================================================================================================
	QList<EditorItem*> _child_items;
	QString _name;
	QIcon   _icon;
	QString _index;
	EditorItem* _parent_item;
};

#endif	//	__EDITOR_ITEM_H__
