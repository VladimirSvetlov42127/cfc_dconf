#ifndef __GRAPHIC_LINE_H__
#define __GRAPHIC_LINE_H__

//===================================================================================================================================================
//	Подключение стандартных библиотек
//===================================================================================================================================================

//===================================================================================================================================================
//	Подключение сторонних библиотек
//===================================================================================================================================================

//===================================================================================================================================================
//	Подключение библиотек QT
//===================================================================================================================================================
#include <QPointF>

//===================================================================================================================================================
//	Подключение модулей проекта
//===================================================================================================================================================
#include <gui/forms/algorithms/custom/flexlogic_namespace.h>


//===================================================================================================================================================
//	Описание класса
//===================================================================================================================================================
class GraphicLine
{
public:
	//===============================================================================================================================================
	//	Конструктор и деструктор класса
	//===============================================================================================================================================
	GraphicLine(const QPointF& begin, const QPointF& end);
	~GraphicLine();

	//===============================================================================================================================================
	//	Методы работы со свойствами класса
	//===============================================================================================================================================
	FlexLogic::LineTypes type() const { return _type; }
	QPointF begin() const { return _begin; }
	QPointF end() const { return _end; }

	//===============================================================================================================================================
	//	Открытые методы класса
	//===============================================================================================================================================
	bool intersects(const GraphicLine line, QPointF& point) const;

private:
	//===============================================================================================================================================
	//	Вспомогательные методы класса
	//===============================================================================================================================================

	//===============================================================================================================================================
	//	Свойства класса
	//===============================================================================================================================================
	FlexLogic::LineTypes _type;
	QPointF _begin;
	QPointF _end;
};

#endif	// __GRAPHIC_LINE_H__
