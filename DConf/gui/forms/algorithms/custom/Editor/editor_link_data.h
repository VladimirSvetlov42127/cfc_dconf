//===================================================================================================================================================
//	Описание класса
//===================================================================================================================================================
class EditorLinkData
{
public:
	//===============================================================================================================================================
	//	Конструктор и деструктор класса
	//===============================================================================================================================================
	EditorLinkData(QString ID = QString());
	~EditorLinkData();

	//===============================================================================================================================================
	//	Методы работы со свойствами класса
	//===============================================================================================================================================
	QString ID() { return _uuid; }
	QString TargetID() { return  _target_id; }
	QString SourceID() { return _source_id; }
	void SetTarget(QString id) { _target_id = id; }
	void SetSource(QString id) { _source_id = id; }

private:
	//===============================================================================================================================================
	//	Свойства класса
	//===============================================================================================================================================
	static inline int _max{ 1 };
	QString _uuid;
	QString _target_id;
	QString _source_id;
};

