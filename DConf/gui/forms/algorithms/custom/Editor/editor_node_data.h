#ifndef __EDITOR_NODE_DATA_H__
#define __EDITOR_NODE_DATA_H__


//===================================================================================================================================================
//	Подключение стандартных библиотек
//===================================================================================================================================================
#include <cstdint>

//===================================================================================================================================================
//	Подключение сторонних библиотек
//===================================================================================================================================================

//===================================================================================================================================================
//	Подключение библиотек QT
//===================================================================================================================================================
#include <QString>

//===================================================================================================================================================
//	Подключение модулей проекта
//===================================================================================================================================================
#include <gui/forms/algorithms/custom/flexlogic_namespace.h>


//===================================================================================================================================================
//	Описание класса
//===================================================================================================================================================
class EditorNodeData
{
public:
	//===============================================================================================================================================
	//	Конструктор и деструктор класса
	//===============================================================================================================================================
	EditorNodeData(QString ID=QString());
	~EditorNodeData();

	//===============================================================================================================================================
	//	Методы работы со свойствами класса
	//===============================================================================================================================================
    QString name() { return _type; }
	QString ID() { return _uuid; }
    uint8_t nodeType() const { return _node_type; }
	bool HasOutput() const { return _has_output; }
	bool Inversion() const { return _inversion; }
	uint8_t MinInputs() const { return _min_inputs; }
	uint8_t MaxInputs() const { return _max_inputs; }
    QList<FlexLogic::NodeParam> paramsList() { return _params_list; }
	void SetOutput(bool has_output) { _has_output = has_output; }
	void SetInversion(bool flag) { _inversion = flag; }
	void InitInputs(int min, int max) { _min_inputs = min; _max_inputs = max; }
	void SetType(QString type) { _type = type; }
	void SetNodeType(uint8_t type) { _node_type = type; }

	//===============================================================================================================================================
	//	Открытые методы класса
	//===============================================================================================================================================
    void addParam(QString name, QString id, QVariant value, QString short_info);
    void addParam(QString name, QString id, QVariant value);
    FlexLogic::NodeParam param(QString id);
    void setParam(QString id, QVariant value);
    void setParam(int index, QVariant value);


private:
	//===============================================================================================================================================
	//	Свойства класса
	//===============================================================================================================================================
	static inline int _max{ 1 };
	QString _uuid;
	QString _type;
	uint8_t _node_type;
	bool _has_output;
	bool _inversion;
	uint8_t _min_inputs;
	uint8_t _max_inputs;
	QList<FlexLogic::NodeParam> _params_list;
};

#endif	//	__EDITOR_NODE_DATA_H__
