//===================================================================================================================================================
//	Конструктор и деструктор класса
//===================================================================================================================================================
Or::Or(QString ID, QSizeF node_size, QGraphicsItem* parent) : EditorNode(ID, parent)
{
    //  Настройка параметров
    SetType("Or");
    SetNodeType(FlexLogic::RZA_OR);
    node_size == QSizeF() ? SetNodeSize(QSizeF(60, 90)) : SetNodeSize(node_size);
    SetOutput(true);
    SetInversion(false);
    InitInputs(2, 16);
    InitSockets();
}

Or::~Or()
{
}


//===================================================================================================================================================
//	Вспомогательные методы класса
//===================================================================================================================================================
void Or::paintElement(QPainter* painter)
{
    QRectF rectangle = AvailableRect();
    QPainterPath path;
    path.addRoundedRect(rectangle, 5, 5);

    //  Вывод и заливка контура элемента
    painter->save();
    painter->setPen(QPen(FlexLogic::shape_color, 2, Qt::SolidLine, Qt::RoundCap, Qt::RoundJoin));
    painter->setRenderHint(QPainter::Antialiasing);
    painter->fillPath(path, FlexLogic::shape_bkcolor);
    painter->drawPath(path);

    //  Вывод текста преобразования
    int text_bottom = 30;
    QRectF textRect(rectangle.topLeft(), QPointF(rectangle.right(), text_bottom));
    painter->setFont(FlexLogic::LABEL_TEXT_FONT);
    painter->drawText(textRect, Qt::AlignCenter, "OR");
    painter->drawLine(rectangle.left(), text_bottom, rectangle.right(), text_bottom);
    painter->restore();

    return;
}
