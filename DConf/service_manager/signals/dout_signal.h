#ifndef __DOUT_SIGNAL_H__
#define __DOUT_SIGNAL_H__


//===================================================================================================================================================
//	Подключение библиотек QT
//===================================================================================================================================================
#include <QString>

//===================================================================================================================================================
//	Подключение модулей проекта
//===================================================================================================================================================
#include "data_model/dc_controller.h"
#include "service_manager/signals/signal.h"


//===================================================================================================================================================
//	Описание класса
//===================================================================================================================================================
//	Базовый класс дискретных выходов
//  Базовые параметры:
//  - Длительность импульса (_impulse_param)
//===================================================================================================================================================
class DoutSignal : public Signal
{
public:
    //===============================================================================================================================================
    //	Конструктор класса
    //===============================================================================================================================================
    DoutSignal(DcController* controller, int32_t id, int32_t internalid, uint8_t direction, uint8_t type, uint8_t subtype, const QString &name, uint16_t subtypeID = 0);

    //===============================================================================================================================================
    //	Открытые методы класса
    //===============================================================================================================================================
    uint16_t impulse() const { return _impulse_param->value().toInt(); }
    void setImpulse(uint16_t value) {_impulse_param->updateValue(QString::number(value)); }

private:
    //===============================================================================================================================================
    //	Свойства класса
    //===============================================================================================================================================
    DcParam* _impulse_param;
};

#endif // __DOUT_SIGNAL_H__
